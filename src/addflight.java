    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
            String fname = jTextField1.getText();
            String fid = jTextField2.getText();
            String source = jTextField3.getText();
            String destination = jTextField4.getText();
            String time = jTextField5.getText();
            String numberofseat = jTextField6.getText();
            

        
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/airlines" , "root" , "123456");
            
            String k = "INSERT INTO flight_details VALUES(?,?,?,?,?,?)";
            PreparedStatement ps = con.prepareStatement(k);
            
            ps.setString(1, fname);
            ps.setString(2, fid);
            ps.setString(3, source);
            ps.setString(4, destination);
            ps.setString(5, time);
            ps.setString(6, numberofseat);
            
            
            
            ps.executeUpdate();
            JFrame jf = new JFrame();
            jf.setAlwaysOnTop(true);
            JOptionPane.showMessageDialog(jf, "New Flight addeded successfully");
            
            setVisible(false);
            new admindeshboard().setVisible(true);


        }
        catch(Exception e)
        {
            JFrame jf = new JFrame();
            jf.setAlwaysOnTop(true);
            JOptionPane.showMessageDialog(jf, e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

